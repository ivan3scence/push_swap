#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	c = c % 128;
	if (c == '\0')
		return ((char *)(&s[ft_strlen(s)]));
	while (*s)
	{
		if (*s == c)
			return ((char *) s);
		s++;
	}
	return (0);
}
