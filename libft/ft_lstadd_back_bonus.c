#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*f;

	if (!(lst && new))
		return ;
	if (*lst)
	{
		f = ft_lstlast(*lst);
		f->next = new;
	}
	else
		*lst = new;
}
