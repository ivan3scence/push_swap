#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	void		*p;
	const void	*s;

	if (!dst && !src)
		return (dst);
	p = dst;
	s = src;
	while (n--)
	{
		*(unsigned char *)(p++) = *(unsigned char *)(s++);
	}
	return (dst);
}
